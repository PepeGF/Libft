#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*node;

	if (!lst)
		return (0);
	node = 0;
	new_lst = 0;
	while (lst)
	{
		node = ft_lstnew(f(lst->content));
		if (!node)
		{
			ft_lstclear(&new_lst, del);
			return (0);
		}
		lst = lst->next;
		ft_lstadd_back(&new_lst, node);
	}
	return (new_lst);
}
